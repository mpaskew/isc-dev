                    <p>Dogs love us, help us, and often entertain us – any way you look at it, dogs are an important part of human life. &nbsp;Whether bringing &nbsp;joy as members of the family, using their keen sense of smell in forensic efforts, or helping people with disabilities navigate with more ease, dogs enrich our lives daily. Dogs and humans have been companions for an estimated 30,000 years but science is only beginning to reveal why dogs bond with us so deeply, and why we bond with our dogs.</p>
                    <p><a href="http://science-is-sexy.com/" onclick="javascript:_gaq.push(['_trackEvent', 'outbound-article', 'http://science-is-sexy.com']);">Science is Sexy</a> is pleased to partner with the <a href="https://www.thebrf.org/" onclick="javascript:_gaq.push(['_trackEvent', 'outbound-article', 'http://www.thebrf.org']);">Brain Research Foundation</a>, <a href="http://www.caninetherapycorps.org/" onclick="javascript:_gaq.push(['_trackEvent', 'outbound-article', 'http://www.caninetherapycorps.org']);">Canine Therapy Corps</a> and the <a href="http://www.anticruelty.org/" onclick="javascript:_gaq.push(['_trackEvent', 'outbound-article', 'http://www.anticruelty.org']);">Anti-Cruelty Society</a> to bring you a fascinating &nbsp;program about the neuroscience of the bond between humans and dogs.</p>
                    <p><a href="http://cnpru.bsd.uchicago.edu/jacobson.html" onclick="javascript:_gaq.push(['_trackEvent', 'outbound-article', 'http://cnpru.bsd.uchicago.edu']);" target="_blank"><img class="alignleft" src="http://cnpru.bsd.uchicago.edu/images/jacobson.jpg" alt="Kristen C. Jacobson, PhD" width="94" height="118"></a>There is growing evidence that the human-animal bond positively influences human emotional and behavioral health, but not much is known about the biological mechanisms that underlie those benefits. At the University of Chicago, <a href="http://cnpru.bsd.uchicago.edu/jacobson.html" onclick="javascript:_gaq.push(['_trackEvent', 'outbound-article', 'http://cnpru.bsd.uchicago.edu']);">Kristen Jacobsen</a>, PhD, is at the forefront of this research to understand why dogs have such positive impacts on our health and happiness.</p>
                    <p>Not only will you get to spend an evening with a leading Chicago scientist, &nbsp;you also can enjoy &nbsp;the company of several therapy dogs from Canine Therapy Corps. These gentle, brilliant dogs motivate individuals recovering from physical and emotional trauma to overcome difficult circumstances including disabilities, injuries, or other challenges by providing goal-directed, interactive therapy as part of the treatment or rehabilitation process. There’s no way that you won’t head home happier and more relaxed after spending some time with these incredible dogs.</p>
